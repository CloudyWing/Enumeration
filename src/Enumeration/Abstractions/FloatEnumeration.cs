        /// <summary>Initializes a new instance of the <see cref="FloatEnumeration{T}" /> class.</summary>
        /// <param name="value">The value.</param>
        protected FloatEnumeration(float value) : base(value) { }

        /// <summary>Initializes a new instance of the <see cref="FloatEnumeration{T}" /> class.</summary>
        /// <param name="value">The value.</param>
        /// <param name="name">The name.</param>
        protected FloatEnumeration(float value, string name) : base(value, name) { }

        /// <summary>Performs an implicit conversion from <see cref="FloatEnumeration{T}" /> to <see cref="double" />.</summary>
        /// <param name="enumeration">The enumeration.</param>
        /// <returns>The result of the conversion.</returns>
        public static implicit operator double(FloatEnumeration<T> enumeration) {
            return enumeration.Value;
        }

        /// <inheritdoc />
        public override bool Equals(object obj) {
            switch (obj) {
                case char c:
                    return Value.Equals(c);
                case byte b:
                    return Value.Equals(b);
                case sbyte sb:
                    return Value.Equals(sb);
                case short s:
                    return Value.Equals(s);
                case ushort us:
                    return Value.Equals(us);
                case int i:
                    return Value.Equals(i);
                case uint ui:
                    return Value.Equals(ui);
                case long l:
                    return Value.Equals(l);
                case ulong ul:
                    return Value.Equals(ul);
            }

            return base.Equals(obj);
        }

        /// <inheritdoc />
        public override int GetHashCode() {
            return base.GetHashCode();
        }

