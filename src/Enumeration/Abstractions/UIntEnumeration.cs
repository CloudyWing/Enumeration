        /// <param name="value">The value.</param>
        protected UIntEnumeration(uint value) : base(value) { }

        /// <summary>Initializes a new instance of the <see cref="UIntEnumeration{T}" /> class.</summary>
        /// <param name="value">The value.</param>
        /// <param name="name">The name.</param>
        protected UIntEnumeration(uint value, string name) : base(value, name) { }

        /// <inheritdoc />
        public override bool Equals(object obj) {
            switch (obj) {
                case char c:
                    return Value.Equals(c);
                case byte b:
                    return Value.Equals(b);
                case ushort us:
                    return Value.Equals(us);
            }

            return base.Equals(obj);
        }

        /// <inheritdoc />
        public override int GetHashCode() {
            return base.GetHashCode();
        }

        /// <summary>Performs an implicit conversion from <see cref="UIntEnumeration{T}" /> to <see cref="long" />.</summary>
        /// <param name="enumeration">The enumeration.</param>
        /// <returns>The result of the conversion.</returns>
        public static implicit operator long(UIntEnumeration<T> enumeration) {
            return enumeration.Value;
